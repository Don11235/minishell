#include "minishell.h"

int	is_quoted(char *token)
{
	int	i;

	i = 0;
	while (token[i] != '\0' && token[i] != '"' && token[i] != '\'')
		i++;
	if (token[i] == '"' || token[i] == '\'')
		return (1);
	return (0);
}
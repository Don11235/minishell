#include "minishell.h"

int	is_unquoted(char *token)
{
	int	i;

	i = 0;
	while (token[i] != '\0' && token[i] != '"' && token[i] != '\'')
		i++;
	if (token[i] == '"' || token[i] == '\'')
		return (0);
	return (1);
}